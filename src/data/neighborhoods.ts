// Get neighborhoods for a city
export const getNeighborhoodsByCity = (city: string, country?: string): string[] => {
  if (!city) return neighborhoodsByCity['Default'];
  // Try disambiguated key first: "City - Country"
  if (country) {
    const composite = `${city} - ${country}`;
    if (neighborhoodsByCity[composite]) return neighborhoodsByCity[composite];
  }
  const list = neighborhoodsByCity[city] || neighborhoodsByCity['Default'];
  // Ensure minimum of 10 entries: if too few, synthesize additional plausible neighborhood names
  const MIN_NEIGHBORHOODS = 10;
  if (list.length >= MIN_NEIGHBORHOODS) return list;
  const padded = [...list];
  const genericTags = ['Centro', 'Norte', 'Sur', 'Este', 'Oeste', 'Residencial', 'Industrial', 'Comercial', 'Universitario', 'Antiguo', 'Nuevo', 'Barrio Alto', 'Barrio Bajo'];
  let i = 0;
  while (padded.length < MIN_NEIGHBORHOODS) {
    const candidate = `${city} ${genericTags[i % genericTags.length]}`;
    if (!padded.includes(candidate)) padded.push(candidate);
    i++;
  }
  return padded;
};
